namespace Magicodes.IE.EPPlus
{
    public enum ExcelImageFormat
    {
        /// <summary>
        /// A bitmap image
        /// </summary>
        Bmp,

        /// <summary>
        /// A jpeg image
        /// </summary>
        Jpeg,

        /// <summary>
        /// A gif image
        /// </summary>
        Gif,

        /// <summary>
        /// A png image
        /// </summary>
        Png,

        /// <summary>
        /// An Enhanced MetaFile image
        /// </summary>
        Emf,

        /// <summary>
        /// A Tiff image
        /// </summary>
        Tif,

        /// <summary>
        /// A windows metafile image
        /// </summary>
        Wmf,

        /// <summary>
        /// A Svg image
        /// </summary>
        Svg,

        /// <summary>
        /// A Webp image
        /// </summary>
        Webp,

        /// <summary>
        /// A Windows icon
        /// </summary>
        Ico,

        /// <summary>
        /// A compressed Enhanced MetaFile image
        /// </summary>
        Emz,

        /// <summary>
        /// A compressed Windows MetaFile image
        /// </summary>
        Wmz
    }
}